package main

import (
	"fmt"
)

// DataType represents a custom data type with a name and a value.
func main() {
	fmt.Println("DataType package initialized")
	fmt.Println("1000 X 200= ", 1000*200)
	fmt.Println("100/3-4*23=", 100/3-4*23)

	fmt.Println(false && true)
	fmt.Println(false || true)
	fmt.Println(!false)

}
